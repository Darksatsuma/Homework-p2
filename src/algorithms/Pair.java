public class Pair {
    int a;
    int b;

    public void setB(int b) {
        this.b = b;
    }

    public void setA(int a) {

        this.a = a;
    }

    public int getA() {

        return a;
    }

    public int getB() {
        return b;
    }

    public Pair(int a, int b) {

        this.a = a;
        this.b = b;
    }
}
